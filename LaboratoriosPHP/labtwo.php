            <h4 style="color:aliceblue">Ciclo de vida de sistemas de información</h4>
            <p style="color:aliceblue">De acuerdo con Jean Carlos (2010) un sistema de información es un conjunto de elementos que trabajan entre sí con el fin de apoyar un proyecto o negocio. De igual manera dice que el ciclo de vida de un sistema de infromación gerencial debe incluir : Planificación, análisis, diseño, implementación y uso y mantenimiento.</p>
            <p style="color:aliceblue">Según Fernando Berzal (S.F.):<br>
                La <b>planificación</b> consiste en la determinación del proyecto y su viabilidad, análisis de riesgos y la asignación de recursos.<br>
                El <b>análisis</b> consiste en la comprension de las necesidades del cliente y el establecimiento de los requerimientos del sistema.<br>
                El <b>diseño</b> consiste en la representación de las caracterísiticas del sistema para realizar una implementación adecuada.<br>
                La <b>implementación</b> consiste en la selección de las herramientas adecuadas para la solución de las necesidades del cliente. En esta parte, es necesario que la programación lleve una estructura decifrable y todos los identificadores relacionados al funcionamiento de una variable.<br>
                El <b>uso y mantenimiento</b> segun Berzal (S.F.) es la etapa más importante del ciclo de vida, ya que mantiene al sistema en funcionamiento mediante 3 etapas: eliminación de defectos, adaptación de nuevas necesidades y adición de nuevas funcionalidades.
            </p>
        </div>
        <div class="col-sm-5" style="background-color:dimgray;">
            <h4 style="color:aliceblue">Ciclo de desarrollo de sistemas de información</h4>
            <p style="color:aliceblue">Según Anónimo (S.F) es un proceso por el cual los analistas de sistemas, los ingenieros de software,
            los programadores y los usuarios finales, se relacionan y estudian la situación actual con el objetivo de
            elaborar un sistema de información<br>
            Las fases de desarrollo según Avelino Felipe Plicarpio (2021) son:<br>
            <b>Identificación del problema, opoartunidades y objetivos:</b> Esta primera fase requiere que el analista obseve objetivamente lo que sucede en un negocio, para así encontrar los problemas y determinar el área de oportunidad del sistema de información. En esta, también es necesario comprender el objetivo del cliente.<br>
            <b>Determinacion de los requerimientos de informacion:</b> En está segunda fase, el analista busca comprender las necesidades e información que el cliente necesita para llevar a cabo sus actividades.<br>
            <b>Diseño del sistema:</b> En esta fase, se producen los detalles que establecen de que manera el sistema cumplirá con los requerimientos de la fase anterior.<br>
            <b>Desarrollo del software:</b> Se desarrollan los programas necesarios para que el sistema funcione. En esta etapa es muy importante que los desarrolladores lleven la docuementación.<br>
            <b>Prueba del sistema:</b> Cada programa es probado. En esta fase, de manera experimental, se asegura de que el sistema no tiene fallas.<br>
            <b>Implementación y evaluación:</b> En está fase, se realiza la instalación y entrenamiento de los usuarios. La evalución es necesaria para identificar los puntos débiles del sistema.
            </p>
